package live_coding.kapitel2_flusskontrolle;

public class WhileSchleife_Wiedergolung_4 {
    public static void main(String[] args) {

        System.out.println("Anfang des Programms");
        int x = 0;

        while (x < 3) { // wird als erstes ausgefuehrt
            System.out.println("Hello World"); // wird als zweites ausgefuehrt
            x++; // wird als drittes ausgefuehrt
        }


        System.out.println("Ende des Programms");


    }
}

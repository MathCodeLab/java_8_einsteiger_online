package live_coding.kapitel4_collections;

public class ArraysClass {
    public static void main(String[] args) {

        // deklarieren und initalisieren
        int x1 = 5;
        System.out.println(x1);
        x1 = 6;

        String[] namen = {"hala", "maya", "darin"};
        System.out.println(namen[0]); // hala
        System.out.println(namen[1]); // maya
        System.out.println(namen[2]); // darin


        int[] noten = {5, 8, 6};
        System.out.println(noten[0]); // 5
        System.out.println(noten[1]); // 8
        System.out.println(noten[2]); // 6
        namen[2] = "Moumena";
        noten[2] = 10;







        // 1. deklarieren
        int x2;

        // 2. initialisiern
        x2 = 5;
    }
}

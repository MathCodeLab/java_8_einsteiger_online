package live_coding.kapitel1_introduction;

public class Operatoren_4 {
    public static void main(String[] args) {


    // Operatoren
    // 1. Arithemeitsch
    // + - * /

    // schreibe ein Program, welches die flaeche eines Rechteck berechnet.
    int laenge = 5;

        System.out.println(laenge);

    int breite = 2;

    int flaeche = laenge * breite;
        System.out.println(flaeche); // 10

    int umfang = 2 * (laenge + breite);
        System.out.println(umfang); // 14


    // deklarieren und intialiseren auf einmal
    int x = 5;
        System.out.println(x); // 5

    // ueberschreiben
    x =3;
    System.out.println(x); // 3

    int y; // deklarieren (definieren)

    // initialisieren: zum ersten mal einen Wert geben / einen Wert zuweisen (geben)
    y =2;
    System.out.println(y); // 2

    // ueberschreiben: den alten Wert mit einem neuen ersezten / einen Wert zuweisen (geben)
    y =7;
    System.out.println(y); // 7


    }

}

package live_coding.kapitel1_introduction;

public class variablen_und_literale_2 {
    public static void main(String[] args) {

        System.out.println("hallo java");

        // Anfuehrungszeichen ""
        System.out.println("merhaba");



        // Variablen:
        // - Datentyp der Variable
        // - Name der Variable
        // - Wert der Variable (Literal)

        // numerische variablen:
        // 1. ganze zahlen: ohne Komma: 6
        int x = 6;

        // 2, gleitkommazahlen: mit Komma: 3.2
        double y = 3.2;


        // ---------------

        // Text (Zeichenkette oder String)
        // Beispiel: "das ist ein belibiger Text"
        String z = "hallo there!";

        // ---------------

        // Einzelnzeichen (charachter)
        // beispiel: A
        char land = 'D';

        double numerDerStrasse = 4.3;

        System.out.println("land");
        System.out.println(land);

        System.out.println("numerDerStrasse"); // numerDe..
        System.out.println(numerDerStrasse); // 4.3
    }
}
